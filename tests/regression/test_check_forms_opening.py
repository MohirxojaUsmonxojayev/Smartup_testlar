@dataclass
class F:
    tab: str
    name: str
    check: str = "Создать"
    sel: str = "b-page"
    tab_role: str = "link"
    tab_exact: bool = False
    item_role: str = "link"
    item_exact: bool = False
    item_scope: str = ""
    item_nth: int = -1
    item_first: bool = False
    reset: bool = False   # True → page.goto(home_url) before navigation
    mbi: bool = False     # True → app-mbi-report-constructor dual-check


_ROLE_SELS = {"heading", "complementary"}


def _navigate(page: Page, spec: F, home_url: str) -> None:
    if spec.reset:
        try:
            page.goto(home_url, wait_until="domcontentloaded", timeout=8000)
        except Exception:
            pass
    page.get_by_role(spec.tab_role, name=spec.tab, exact=spec.tab_exact).click()
    base = page.locator(spec.item_scope) if spec.item_scope else page
    locator = base.get_by_role(spec.item_role, name=spec.name, exact=spec.item_exact)
    if spec.item_nth >= 0:
        locator = locator.nth(spec.item_nth)
    elif spec.item_first:
        locator = locator.first
    locator.click()


def _verify(page: Page, spec: F) -> None:
    if spec.mbi:
        try:
            page.wait_for_selector("app-mbi-report-constructor, b-page", timeout=15000)
        except Exception:
            pass
        if page.locator("app-mbi-report-constructor").count() > 0:
            expect(page.locator("app-mbi-report-constructor")).to_contain_text(
                spec.check, timeout=10000
            )
        else:
            expect(page.locator("b-page")).to_contain_text(spec.check, timeout=10000)
    elif spec.sel in _ROLE_SELS:
        expect(page.get_by_role(spec.sel)).to_contain_text(spec.check)
    else:
        expect(page.locator(spec.sel)).to_contain_text(spec.check)


def _open_form(page: Page, spec: F, home_url: str, soft: SoftAssert) -> None:
    label = f"[{spec.tab}] {spec.name}"
    with allure.step(label):
        try:
            _navigate(page, spec, home_url)
            _verify(page, spec)
        except Exception as e:
            soft.check(False, f"❌ {label} — '{spec.check}' topilmadi | {type(e).__name__}")


